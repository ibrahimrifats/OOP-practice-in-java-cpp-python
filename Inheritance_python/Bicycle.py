class Bicycle:
    def __init__(self):
        self.cadence = 0
        self.gear = 0
        self.speed = 0
        
    def setCadence(self, newValue):
        self.cadence = newValue
        
    def setGear(self, newValue):
        self.gear = newValue
        
    def setSpeed(self, newValue):
        self.speed = newValue
        
    
        
     